function adicao (num1, num2) {
    return num1 + num2
}


function subtracao (num1, num2) {
    return num1 - num2
}


function multiplicacao (num1, num2) {
    return num1 * num2
}


function divisao (num1, num2) {
   return num1 / num2
}


function restodivisao (num1, num2) {
   return num1 % num2
}


console.log ("O resultado da adição é: ", adicao (4, 5))
console.log ("O resultado da subtração é: ", subtracao (7, 4))
console.log ("O resultado da multiplicação é: ", multiplicacao (6, 8))
console.log ("O resultado da divisão é: ", divisao (20, 2))
console.log ("O resto da divisão é: ", restodivisao (20, 2))


